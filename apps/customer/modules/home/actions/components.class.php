    public function textWidgetLinkTo($themeOptions, $TextWidgetsLinkType, $TextWidgetsLinkId)
    {
		if($themeOptions[$TextWidgetsLinkType] != "" && $themeOptions[$TextWidgetsLinkId] != "")
		{
			$url = "";
			if($themeOptions[$TextWidgetsLinkType] == 1)
			{
				$objectCmsPage = Doctrine::getTable('CMSPages')->find(array($themeOptions[$TextWidgetsLinkId]));
				$slug = $objectCmsPage->getSlug();
				$url = url_for('/'.$slug, true);
			}
			elseif($themeOptions[$TextWidgetsLinkType] == 2)
			{
				$objectPracticeArea = Doctrine::getTable('WebsitePracticeArea')->find(array($themeOptions[$TextWidgetsLinkId]));
				$slug = $objectPracticeArea->getSlug();
				$url = url_for('/practice-area/'.$slug, true);
			}

			return $url;
		}
		return false;
    }
    /**
     * Function to Get No of Text Widget of Theme
     *
     * @param unknown_type $widgetNuber
     */
    public function executeTextWidget1($widgetNuber){
        $widgetNo = $this->widgetNumber ;
        $websiteId = $this->context->get('WebsiteId');
        $themeOptions = Doctrine_Core::getTable('ThemeOptions')->getThemeOptions($websiteId);

        if(array_key_exists("TextWidgetsLinkType_".$widgetNo,$themeOptions) && array_key_exists("TextWidgetsLinkId_".$widgetNo, $themeOptions))
			$this->textWidgetLink = $this->textWidgetLinkTo($themeOptions, "TextWidgetsLinkType_".$widgetNo,"TextWidgetsLinkId_".$widgetNo);
        else
			$this->textWidgetLink = "";

        $this->widgetTitle = $themeOptions['TextWidgetsTitle_'.$widgetNo] ;
        $this->widgetContent = $themeOptions['TextWidgets_'.$widgetNo] ;
    }
